#include <stdio.h>

int main(void) {
    char c[][9] = {
        {'*',' ','*',' ','*',' ','*',' ','*'},
        {'*',' ',' ',' ',' ',' ',' ',' ','*'},
        {'*',' ',' ',' ',' ',' ',' ',' ','*'},
        {'*',' ',' ',' ',' ',' ',' ',' ','*'},
        {'*',' ','*',' ','*',' ','*',' ','*'}
    };

    int i, j;
    for (i = 0; i < 5; i++) {
        for (j = 0; j < 9; j++) {
            printf("%c", c[i][j]);
        }
        printf("\n"); // 换行
    }
    return 0;
}